# String manipulation

sentence = "money!is!the!root!of!evil"
# This code writes the string in reverse
reverse_sentence = sentence[:: -1]
# This code writes the string uppercase letters
upper_sentence = (sentence.upper()) 
# This codes replaces the exclamation mark with space in the program
new_sentence = sentence.replace("!", " ")

# This code prints all the sentences
print('String')
print (sentence)
print('String without exclamation marks')
print (new_sentence)
print('String in uppercase letters')
print (upper_sentence)
print('String reversed')
print (reverse_sentence)
